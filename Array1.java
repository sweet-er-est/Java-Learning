public class Array1{
    public static void main(String[] args){
        
        int marks[] = new int[5];
        marks[0] = 60;
        marks[1] = 70;
        marks[2] = 77;
        marks[3] = 68;
        marks[4] = 82;
        for(int i=0;i<5;i=i+1){
            System.out.print(marks[i] + "\n");
        }

    }
}